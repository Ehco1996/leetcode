# [704] 二分查找
#

# @lc code=start
class Solution:
    def search(self, nums, target):
        left = 0
        right = len(nums) - 1
        while left <= right:
            mid = left + (right - left) // 2
            if nums[mid] < target:
                left = mid + 1
            elif nums[mid] > target:
                right = mid - 1
            elif nums[mid] == target:
                return mid
        return -1

    def test(self, nums=[-1, 0, 3, 5, 9, 12], target=9):
        return self.search(nums, target)



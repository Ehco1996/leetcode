# @lc code=start
class Solution:
    def isSubsequence(self, s: str, t: str) -> bool:
        """双指针，一个指向s一个指向t向前走"""
        i = 0
        j = 0
        while i < len(s) and j < len(t):
            if s[i] == t[j]:
                i += 1
            j += 1
        return i==len(s)



# @lc code=start
class Solution:
    def removeElement(self, nums, val) -> int:
        """快慢指针，因为数组是排序的"""
        if not nums:
            return 0
        slow = 0
        fast = 0
        while fast < len(nums):
            if nums[fast] != val:
                nums[slow] = nums[fast]
                slow += 1
            fast += 1
        return slow


